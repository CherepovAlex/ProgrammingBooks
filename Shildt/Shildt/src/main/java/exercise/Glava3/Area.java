package exercise.Glava3;

public class Area {
    public static void main(String[] args) {
        double pi, r, a;

        r = 10.8;       // радиус круга
        pi = 3.14;      // приближенное значение Pi
        a = pi * r * r; // вычислить площадь

        System.out.println("Area of circle is " + a);
    }
}
