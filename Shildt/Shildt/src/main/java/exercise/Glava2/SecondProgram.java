package exercise.Glava2;

public class SecondProgram {
    public static void main(String[] args) {
        int num;
        num = 100;
        System.out.println("Value num: " + num);
        num = num * 2;
        System.out.print("Value num * 2: ");
        System.out.println(num);
    }
}

// Значение num: 100
// Значение num  *  2: 200