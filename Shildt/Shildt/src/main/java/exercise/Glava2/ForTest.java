package exercise.Glava2;
//  короткая программа, иллюстрирующая работу цикла for
public class ForTest {
    public static void main(String[] args) {
        int x;

        for (x  = 0; x < 10; x = x + 1) {
            System.out.println("Value x: " + x);
        }
    }
}

