package BinarySearch._35SearchInsertPosition;
// 35. Позиция вставки при поиске
// Дан отсортированный массив различных целых чисел и целевое значение.
// Если целевое значение найдено, верните индекс.
// Если нет, верните индекс, по которому оно было бы вставлено, если бы его вставили по порядку.
// Вы должны написать алгоритм со сложностью выполнения O(log n) за время.
// Пример 1:
//Входные данные: nums = [1,3,5,6], target = 5
//Выходные данные: 2
//Пример 2:
//Входные данные: nums = [1,3,5,6], target = 2
//Выходные данные: 1
//Пример 3:
//Входные данные: nums = [1,3,5,6], target = 7
//Выходные данные: 4
public class Solution {
    // time O(log n), space O(1)
    public int searchInsert(int[] nums, int target) {
        // Инициализация левой границы поиска - начало массива
        int left = 0;   // начальный индекс (0, начало массива)
        // Инициализация правой границы поиска - конец массива
        int right = nums.length - 1;    // конечный индекс (длина массива - 1, конец массива)
        // Пока левая граница не превысит правую, продолжаем поиск
        while (left <= right) { // Цикл выполняется, пока левая граница не превысит правую
            // Вычисление среднего индекса для деления диапазона пополам
            int mid = left + (right - left) / 2;    // Вычисление среднего индекса: для избежания возможного
            // Если элемент в средней позиции равен целевому значению
            if (nums[mid] == target) {              // переполнения при больших значениях индексов
                 // Возвращаем индекс найденного элемента
                return mid;
            // Если элемент в средней позиции меньше целевого значения
            } else if (nums[mid] < target) {
                // Сдвигаем левую границу поиска за средний элемент
                // Ищем в правой половине массива
                left = mid + 1;
             // Если элемент в средней позиции больше целевого значения
            } else {
                // Сдвигаем правую границу поиска перед средним элементом
                // Ищем в левой половине массива
                right = mid - 1;
            }
        }
        // Если элемент не найден, возвращаем позицию для вставки
        // После завершения цикла left указывает на позицию, куда нужно вставить target
        // для сохранения отсортированного порядка массива
        return left;
    }
    // Ключевые моменты алгоритма:
    //Бинарный поиск - обеспечивает сложность O(log n)
    //Возврат left - когда цикл завершается, left всегда указывает на правильную позицию для вставки:
    //Для существующих элементов - возвращается их индекс
    //Для отсутствующих элементов - возвращается позиция, где они должны быть
    //Безопасное вычисление mid - предотвращает переполнение для больших массивов
    //Алгоритм оптимален как по времени выполнения, так и по используемой памяти.

    public static void main(String[] args) {
        int[] nums1 = {1, 3, 5, 6};
        int target1 = 5;
        int target2 = 2;
        int target3 = 7;
        System.out.println(new Solution().searchInsert(nums1, target1));    // 2
        System.out.println(new Solution().searchInsert(nums1, target2));    // 1
        System.out.println(new Solution().searchInsert(nums1, target3));    // 4
    }

}
