package BitManipulation._136SingleNumber;

import java.util.HashMap;
import java.util.Map;
// 136. Одиночное число
// Дан непустой массив целых чисел nums, в котором каждый элемент встречается дважды,
// кроме одного. Найдите этот единственный элемент.
//Вы должны реализовать решение с линейной сложностью выполнения и использовать
// только постоянное дополнительное пространство
// Example 1:
//Input: nums = [2,2,1]
//Output: 1
//Example 2:
//Input: nums = [4,1,2,1,2]
//Output: 4
//Example 3:
//Input: nums = [1]
//Output: 1
public class Solution {
    // version 1
    public int singleNumber(int[] nums) {
        // Создаем HashMap для хранения чисел и количества их вхождений
        Map<Integer, Integer> map = new HashMap<>();
        // Начинаем цикл для перебора всех элементов массива
        for (int i = 0; i < nums.length; i++) {
            // Проверяем, содержится ли текущее число уже в HashMap
            if (map.containsKey(nums[i])) {
                // Если число уже есть в HashMap, увеличиваем счетчик вхождений на 1
                map.put(nums[i], map.get(nums[i]) + 1);
            } else {
                // Если числа нет в HashMap, добавляем его со счетчиком 1
                map.put(nums[i], 1);
            }
        }
        // Получаем набор всех пар "ключ-значение" из HashMap для итерации
        for (Map.Entry<Integer, Integer> entry : map.entrySet()) {
            // Проверяем, равно ли количество вхождений текущего числа 1
            if (entry.getValue() == 1) {
                // Если да, возвращаем это число (одиночное число найдено)
                return entry.getKey();
            }
        }
         // Возвращаем -1 если одиночное число не найдено
        // (по условию задачи массив непустой, поэтому эта строка никогда не выполнится)
        return -1;
    }
    // Версия 1 с HashMap менее оптимальна, так как:
    //Использует O(n) дополнительной памяти для хранения HashMap
    //Имеет больше накладных расходов на операции вставки и поиска в HashMap
    //Требует двух проходов: один для заполнения HashMap, второй для поиска элемента

    // version 2
    // Оптимальное решение с использованием XOR
    public int singleNumber2(int[] nums) {
        // Инициализируем результат нулем
        int result = 0;
        // Проходим по всем элементам массива
        for (int num : nums) {
            // Применяем операцию XOR между текущим результатом и элементом массива
            result ^= num;
        }
        // Возвращаем результат - одиночное число
        return result;
    }
    // Версия 2 (оптимальная) использует операцию XOR (исключающее ИЛИ), которая обладает следующими свойствами:
    //a ^ a = 0 (число XOR само с собой дает 0)
    //a ^ 0 = a (число XOR с 0 дает само число)
    //Операция коммутативна и ассоциативна: a ^ b ^ a = (a ^ a) ^ b = 0 ^ b = b
    // Преимущества этого решения:
    //Временная сложность: O(n) - один проход по массиву
    //Пространственная сложность: O(1) - используем только одну переменную
    //Производительность: Быстрее версии с HashMap, так как нет накладных расходов на работу с коллекциями

    public static void main(String[] args) {
        int[] nums1 = new int[]{2, 2, 1};
        System.out.println(new Solution().singleNumber(nums1)); //  1
    }
}
