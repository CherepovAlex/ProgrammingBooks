package TwoPointers._42TrappingRainWater;
// 42. Сбор дождевой воды
// Дано n неотрицательных целых чисел, представляющих собой карту высот, где ширина каждой полосы равна 1.
// Вычислите, сколько воды может скопиться на ней после дождя.
// Example 1:
//Input: height = [0,1,0,2,1,0,1,3,2,1,2,1]
//Output: 6
// Explanation: The above elevation map (black section) is represented by array [0,1,0,2,1,0,1,3,2,1,2,1].
// In this case, 6 units of rain water (blue section) are being trapped.
//Example 2:
//Input: height = [4,2,0,3,2,5]
//Output: 9
public class Solution {
    // time O(n), space O(n)
    // Более интуитивно понятный, Легче для понимания и отладки
    public int trap(int[] height) {
        int n = height.length;  // получаем длину массива высот
        if (n < 3) return 0;    // если меньше 3 элементов, вода не может удерживаться

        // Создаем массив для хранения максимумов слева
        int[] left = new int[n];
        for (int i = 0; i < n; i++) {   // Проходим по массиву слева направо
            // left[i] содержит максимальную высоту слева от позиции i (включая саму позицию i)
            left[i] = i == 0 ? height[i] : Math.max(left[i - 1], height[i]); // Записываем текущий максимум слева
        }

        // Создаем массив для хранения максимумов справа
        int[] right = new int[n];
        for (int i = n - 1; i >= 0; i--) {  // Проходим по массиву справа налево
            // right[i] содержит максимальную высоту справа от позиции i (включая саму позицию i)
            right[i] = i == n - 1 ? height[i] : Math.max(right[i + 1], height[i]); // Записываем текущий максимум справа
        }
        // Вычисление общего объема воды:
        int sum = 0;                    // Инициализируем счетчик воды
        for (int i = 0; i < n; i++) {   // Проходим по всем позициям
            sum += Math.min(right[i], left[i]) - height[i]; // Вода = минимум(левый_макс, правый_макс) - текущая_высота
        } // Для каждой позиции i: количество воды = минимальная высота "стенок" слева и справа
        return sum;                                      // минус высота самой позиции
    }
    // time O(n), space O(1)
    // Более эффективен по памяти, Использует умную логику двух указателей
    public int trap1(int[] height) {
        int n = height.length;              // Получаем длину массива высот
        if (n < 3) return 0;                // Если меньше 3 элементов, вода не может удерживать

        int left = 0, right = n - 1;        // Инициализируем два указателя - с начала и конца массива
        int leftMax = 0, rightMax = 0;      // Инициализируем максимальные высоты слева и справа
        int water = 0;                      // Инициализируем счетчик воды

        while (left <= right) {             // Пока указатели не встретились
            if (height[left] <= height[right]) {    // Если высота слева меньше или равна высоте справа
                if (height[left] > leftMax) {       // Если текущая высота больше левого максимума
                    leftMax = height[left];         // Обновляем левый максимум
                } else {                            // Иначе есть "впадина"
                    water += leftMax - height[left]; // Добавляем воду: разница между максимумом и текущей высотой
                }
                left++;                             // Двигаем левый указатель вправо
            } else {                                // Если высота справа меньше высоты слева
                if (height[right] > rightMax) {     // Если текущая высота больше правого максимума
                    rightMax = height[right];       // Обновляем правый максимум
                } else {                            // Иначе есть "впадина"
                    water += rightMax - height[right]; // Добавляем в воду: разницу между максимумом и текущей высотой
                }
                right--;                             // Двигаем правый указатель влево
            }
        }
        return water;                               // Возвращаем общее количество воды
    }

    public static void main(String[] args) {
        int[] height = {0, 1, 0, 2, 1, 0, 1, 3, 2, 1, 2, 1};
        System.out.println(new Solution().trap(height));

    }
}
