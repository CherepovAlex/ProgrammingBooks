package TwoPointers._344ReverseString;
import java.util.Arrays;

// 344. Обратная строка
// Напишите функцию, которая переворачивает строку. Входная строка задаётся в виде массива символов s.
// Для этого необходимо изменить входной массив на месте с помощью O(1) дополнительной памяти.
// Example 1:
//Input: s = ["h","e","l","l","o"]
//Output: ["o","l","l","e","h"]
//Example 2:
//Input: s = ["H","a","n","n","a","h"]
//Output: ["h","a","n","n","a","H"]
public class Solution {
    // time O(n), space O(1)
    public void reverseString(char[] s) {
        // Инициализируем два указателя: left в начале массива, right в конце
        int left = 0;
        int right = s.length - 1;

        // Пока левый указатель меньше правого (не встретились)
        while (left < right) {
            // Сохраняем символ с левой позиции во временную переменную
            char temp = s[left];
            // Заменяем символ на левой позиции символом с правой позиции
            s[left] = s[right];
            // Заменяем символ на правой позиции сохранённым символом с левой позиции
            s[right] = temp;
            // Сдвигаем левый указатель вправо
            left++;
            // Сдвигаем правый указатель влево
            right--;
        }
    }
    // Ключевые особенности оптимального решения:
    //Алгоритм двух указателей - наиболее эффективный подход
    //In-place модификация - не создаётся новый массив
    //Временная сложность: O(n) - проходим по половине массива
    //Пространственная сложность: O(1) - только несколько переменных
    //Попарный обмен - на каждой итерации меняем местами два символа
    //Решение идеально соответствует требованиям задачи!

    public static void main(String[] args) {
        char[] chars = new char[]{'h', 'e', 'l', 'l', 'o'};
        new Solution().reverseString(chars);
        System.out.println(Arrays.toString(chars));
    }
}
