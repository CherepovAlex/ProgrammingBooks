package DynamicProgramming._53MaximumSubarray;

// 53. Максимальный подмассив
//Дан целочисленный массив nums. Найдите подмассив (идущих подряд элементов) с наибольшей суммой и верните его сумму.
// Example 1:
//Input: nums = [-2,1,-3,4,-1,2,1,-5,4]
//Output: 6
//Explanation: The subarray [4,-1,2,1] has the largest sum 6.
//Example 2:
//Input: nums = [1]
//Output: 1
//Explanation: The subarray [1] has the largest sum 1.
//Example 3:
//Input: nums = [5,4,-1,7,8]
//Output: 23
//Explanation: The subarray [5,4,-1,7,8] has the largest sum 23.
public class Solution {
    // Алгортим Кадане работает по принципу "локальной" и "глобальной" оптимальности.
    // На каждом шаге мы решаем: продолжить текущий подмассив или начать новый с текущего элемента.
    // time O(n) space O(1)
    public int maxSubArray(int[] nums) {
        // Проверяем, пуст ли массив (по условию массив не пустой, но для надёжности)
        if (nums == null || nums.length == 0) return 0;

        // Инициализируем переменную для хранения максимальной суммы подмассива
        // Начинаем с первого элемента массива
        int maxSum = nums[0];

        // Инициализируем переменную для хранения текущей суммы подмассива
        // Начинаем с первого элемента массива
        int currentSum = nums[0];

        // Проходим по всем элементам массива, начиная со второго
        for (int i = 1; i < nums.length; i++) {
            // Для каждого элемента выбираем максимум между:
            // 1. Начать новый подмассив с текущего элемента
            // 2. Продолжить текущий подмассив, добавив текущий элемент
            currentSum = Math.max(nums[i], currentSum + nums[i]);

            // Обновляем глобальный маскимум, если текущая сумма больше
            maxSum = Math.max(maxSum, currentSum);
        }

        // Возвращаем найденную максимальную сумму
        return maxSum;
    }

    public int maxSubArray1(int[] nums) {
        // Проверка на пустой массив (хотя по условию массив всегда содержит элементы)
        if (nums == null || nums.length == 0) {
            return 0;
        }

        // maxSum хранит максимальную сумму подмассива за все время
        // Инициализируем первым элементом массива
        int maxSum = nums[0];

        // currentSum хранит сумму текущего подмассива
        // Инициализируем первым элементом массива
        int currentSum = nums[0];

        // Проходим по всем элементам массива, начиная со второго (индекс 1)
        for (int i = 1; i < nums.length; i++) {
            // Ключевая идея алгоритма Кадане:
            // На каждом шаге мы решаем - продолжить текущий подмассив или начать новый
            // Если текущая сумма отрицательная, лучше начать новый подмассив с текущего элемента
            // Если текущая сумма положительная, выгодно добавить текущий элемент к существующему подмассиву
            if (currentSum < 0) {
                // Текущая сумма отрицательная - начинаем новый подмассив с текущего элемента
                currentSum = nums[i];
            } else {
                // Текущая сумма положительная - добавляем текущий элемент к подмассиву
                currentSum += nums[i];
            }

            // Сравниваем текущую сумму с глобальным максимумом и обновляем при необходимости
            if (currentSum > maxSum) {
                maxSum = currentSum;
            }
        }

        // Возвращаем максимальную сумму подмассива
        return maxSum;
    }

    public static void main(String[] args) {
        int[] nums = {-2, 1, -3, 4, -1, 2, 1, -5, 4};
        System.out.println(new Solution().maxSubArray(nums)); // 6
    }
}
